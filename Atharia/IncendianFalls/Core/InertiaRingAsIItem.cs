  public IInertiaItem AsIInertiaItem() {
    return new InertiaRingAsIInertiaItem(obj);
  }

         public Void Destruct() {
    return InertiaRingExtensions.Destruct(obj);
  }
  public IItem ClonifyAndReturnNewReal(Root newRoot) {
    return InertiaRingExtensions.ClonifyAndReturnNewReal(obj, newRoot);
