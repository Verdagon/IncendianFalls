using System;
using System.Collections;

using System.Collections.Generic;

namespace Atharia.Model {

public class ManaPotionAsIUnitComponent : IUnitComponent {
  public readonly ManaPotion obj;
  public int id => obj.id;
  public Root root => obj.root;
  public void Delete() { obj.Delete(); }
  public bool Exists() { return obj.Exists(); }
  public ManaPotionAsIUnitComponent(ManaPotion obj) {
    this.obj = obj;
  }
  public void FindReachableObjects(SortedSet<int> foundIds) {
    obj.FindReachableObjects(foundIds);
  }
  public bool Is(IImmediatelyUseItem that) {
    if (!this.Exists()) {
      throw new Exception("Called Is on a null!");
    }
    if (!that.Exists()) {
      throw new Exception("Called Is on a null!");
    }
    return root == that.root && obj.id == that.id;
  }
  public bool NullableIs(IImmediatelyUseItem that) {
    if (!this.Exists() && !that.Exists()) {
      return true;
    }
    if (!this.Exists() || !that.Exists()) {
      return false;
    }
    return this.Is(that);
  }
  public IImmediatelyUseItem AsIImmediatelyUseItem() {
    return new ManaPotionAsIImmediatelyUseItem(obj);
  }
  public bool Is(IDestructible that) {
    if (!this.Exists()) {
